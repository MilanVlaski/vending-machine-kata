package main;

public enum ValidCoin {

	QUARTER(0.25),
	DIME(0.10),
	NICKEL(0.05);
	
	public double value;
	
	ValidCoin (double value) {
		this.value = value;
	}
	
	public static double valueOfCoin(String typeOfCoin) {
		
		String type = typeOfCoin.toUpperCase();
		double result = 0;
		for (ValidCoin c : ValidCoin.values()) {
			if(type.equals(c.toString()))
				result = c.value;
		}
		
		return result;
	}
}
